#include "ft_printf.h"

int	ft_isflag(char a)
{
	int	i;

	i = 0;
	a == '-' ? (i = 1) : 0;
	a == '+' ? (i = 1) : 0;
	a == ' ' ? (i = 1) : 0;
	a == '#' ? (i = 1) : 0;
	a == '0' ? (i = 1) : 0;
	return (i);
}
