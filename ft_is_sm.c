#include "ft_printf.h"

int	ft_is_sm(char a)
{
	int	i;

	i = 0;
	a == 'h' ? (i = 1) : 0;
	a == 'l' ? (i = 1) : 0;
	a == 'j' ? (i = 1) : 0;
	a == 'z' ? (i = 1) : 0;
	return (i);
}
